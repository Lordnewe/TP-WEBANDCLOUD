package tinypet;

public class Sign {

	public String mail;
	public String signedPet;

	public Sign() {}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getSignedPet() {
		return signedPet;
	}

	public void setSignedPet(String signedPet) {
		this.signedPet = signedPet;
	}

}
